import React, { useEffect, useState } from 'react'
import '../App.css';

export default function AppStats() {
    const [isLoaded, setIsLoaded] = useState(false);
    const [stats, setStats] = useState({});
    const [error, setError] = useState(null)

	const getStats = () => {

        fetch(`http://ec2-3-93-190-194.compute-1.amazonaws.com:8100/stats`)
            .then(res => res.json())
            .then((result)=>{
				console.log("Received Stats")
                setStats(result);
                setIsLoaded(true);
            },(error) =>{
                setError(error)
                setIsLoaded(true);
            })
    }
    useEffect(() => {
		const interval = setInterval(() => getStats(), 2000); // Update every 2 seconds
		return() => clearInterval(interval);
    }, [getStats]);

    if (error){
        return (<div className={"error"}>Error found when fetching from API</div>)
    } else if (isLoaded === false){
        return(<div>Loading...</div>)
    } else if (isLoaded === true){
        return(
            <div>
                <h1>Latest Stats</h1>
                <table className={"StatsTable"}>
					<tbody>
						<tr>
							<th>Dispenses</th>
							<th>Refills</th>
						</tr>
						<tr>
							<td># Dispenses: {stats['num_dispense_records']}</td>
							<td># Refills: {stats['num_refill_records']}</td>
						</tr>
						<tr>
							<td colspan="2">Highest Payment: ${stats['max_dispense_amount_paid']}</td>
						</tr>
						<tr>
							<td colspan="2">Most Items Refilled: {stats['max_refill_quantity']}</td>
						</tr>
					</tbody>
                </table>
                <h3>Last Updated: {stats['last_updated']}</h3>

            </div>
        )
    }
}
